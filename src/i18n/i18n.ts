// ── Runtime state ─────────────────────────────────────────────────────────────

let _lang: Lang = 'en';

export const i18n = {
  /** Инициализировать из globalState при активации расширения */
  init(ctx: vscode.ExtensionContext): void {
    _lang = ctx.globalState.get<Lang>(LANG_KEY, 'en');
  },

  /** Сменить язык и сохранить в globalState */
  async set(lang: Lang, ctx: vscode.ExtensionContext): Promise<void> {
    _lang = lang;
    await ctx.globalState.update(LANG_KEY, lang);
  },

  getLang(): Lang { return _lang; },

  /** Текущий набор строк */
  get s(): Strings { return S[_lang]; },
};
